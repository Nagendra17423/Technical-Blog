package com.tech.blog.entities;

public class Category {
	private int cid;
	private String ctitle;
	private String cdisp;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCtitle() {
		return ctitle;
	}
	public void setCtitle(String ctitle) {
		this.ctitle = ctitle;
	}
	public String getCdisp() {
		return cdisp;
	}
	public void setCdisp(String cdisp) {
		this.cdisp = cdisp;
	}
	

}
